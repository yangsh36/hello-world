public class DogSize {
    public static void main(String[] args) {
        int dogSize = 20;
        if (dogSize >= 50) {
        	System.out.println("Woof");
        }
        else if (dogSize >= 10) {
        	System.out.println("Bark");
        }
        else {
        	System.out.println("Yip");
        }
    }
}
